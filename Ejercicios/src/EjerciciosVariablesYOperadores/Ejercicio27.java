package EjerciciosVariablesYOperadores;

import java.util.Scanner;

public class Ejercicio27 {
    public static void main(String[] args) {
        /*
        Un alumno desea saber cual será su calificación final en la materia de Algoritmos. Dicha
calificación se compone de los siguientes porcentajes:
a. 55% del promedio de sus tres calificaciones parciales.
b. 30% de la calificación del examen final.
c. 15% de la calificación de un trabajo final.
         */
        Scanner sc = new Scanner(System.in);
        System.out.println("Calificación Parcial 1: ");
        double calificacion1 = sc.nextDouble();

        System.out.println("Calificación Parcial 2: ");
        double calificacion2 = sc.nextDouble();

        System.out.println("Calificación Parcial 3: ");
        double calificacion3 = sc.nextDouble();

        double MediaParciales = (calificacion1 + calificacion2 + calificacion3) / 3;
        double PromedioParciales = MediaParciales * 0.55;

        System.out.println("Calificación Examen Final: ");
        double CalificacionFinal = sc.nextDouble();

        double PorcentajeCF = CalificacionFinal * 0.3;

        System.out.println("Calificación Trabajo final: ");
        double TrabajoFinal = sc.nextDouble();

        double PorcentajeTF = TrabajoFinal * 0.15;
        double CalificacionTotal = PromedioParciales + PorcentajeTF + PorcentajeCF;

        System.out.printf("Nota final: %.2f", CalificacionTotal);
    }
}















































































































































































































































































































































































































































































































































































































































































