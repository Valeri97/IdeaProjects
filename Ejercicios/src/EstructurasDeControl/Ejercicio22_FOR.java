package EstructurasDeControl;

public class Ejercicio22_FOR {
    public static void main(String[] args) {
        /*
        Hacer un programa que escriba los valores comprendidos entre 531 y 540 (inclusive).
         */
        for (int a = 531; a >= 531 && a <= 540; a++){
            System.out.println(a);
        }
    }
}
